    <h4>Perpustakaan SMECHATWOLASMA</h4>
    Perpustakaan Smechatwolasma merupakan sebuah fasilitas sekolah yang didirikan pada bulan Januari, 2005. Pada awalnya, perpustakaan ini menggunakan
    nama Perpustakaan SMA 12 yang berada di bawah naungan SMAN 12 Malang, hingga kemudian pada tahun 2007, SMAN 12 Malang berganti menjadi SMKN 12 Malang. Semenjak diterbitkan Surat Keputusan/SK Perpustakaan dari Kepala SMK Negeri 12 Malang Nomor: 421.5/229.35.73.SMKN12/2007
    Tentang Pendirian Perpustakaan Sekolah Menengah Kejuruan Negeri 12 Malang, nama perpustakaan berubah menjadi PERPUSTAKAAN SMECHATWOLASMA. Perpustakaan Smechatwolasma berfungsi sebagai pusat belajar mengajar, pusat informasi, pusat penelitian sederhana dan rekreasi sehat melalui bahan pustaka.

    <p>
    <h4>Fungsi Perpustakaan</h4>
    Fungsi Perpustakaan
    Dalam kaitan dengan kurikulum yang diterapkan di SMK Negeri 12 Malang, perpustakaan sekolah berfungsi:

    1.Wadah pengetahuan, administrasi, organisasi dan rekreatif yang sesuai sehingga memudahkan penggunaannya;
    2.Sumber rujukan (reference centre) siswa, guru, tenaga bimbingan, tenaga administrasi dan pegawai yang berada dibawah naungan SMK Negeri 12 Malang.
    3.Sarana pendukung dalam proses belajar mengajar, guna mencapai tujuan pendidikan nasional;
    4.Pusat informasi untuk menyediakan bahan-bahan yang bermanfaat bagi kegiatan penunjang kegiatan belajar mengajar, seperti kegiatan yang berkaitan dengan budaya, seni, kreasi dan budaya bagi kegiatan belajar mengajar;

